package aula07_31_03.ex03;

public class Quarto {
    int numeroQuarto;
    String tipo;
    double valorDiaria;
    boolean disponivel;

    public Quarto(int numeroQuarto, String tipo, double valorDiaria, boolean disponivel) {
        this.numeroQuarto = numeroQuarto;
        this.tipo = tipo;
        this.valorDiaria = valorDiaria;
        this.disponivel = disponivel;
    }
    
}
